export function lookupFundWebsite(fundId) {
  if (!fundId) return "";
  const id = fundId.toLowerCase().trim();
  if (FUND_WEBSITES[id]) return FUND_WEBSITES[id];
  for (const suffix of ["-capital", "-ventures", "-vc", "-partners"]) {
    if (FUND_WEBSITES[id + suffix]) return FUND_WEBSITES[id + suffix];
  }
  for (const [key, url] of Object.entries(FUND_WEBSITES)) {
    if (id.includes(key) || key.includes(id)) return url;
  }
  return "";
}

